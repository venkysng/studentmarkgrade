package com.venkytech.studentmarkgrade.model;

public class TeacherPercentAllocation {

	private double assignmentPercent;
	private double examsPercent;
	private double creditAssignmentPerecent;


	public double getAssignmentPercent() {
		return assignmentPercent;
	}
	public void setAssignmentPercent(double assignmentPercent) {
		this.assignmentPercent = assignmentPercent;
	}
	public double getExamsPercent() {
		return examsPercent;
	}
	public void setExamsPercent(double examsPercent) {
		this.examsPercent = examsPercent;
	}
	public double getCreditAssignmentPerecent() {
		return creditAssignmentPerecent;
	}
	public void setCreditAssignmentPerecent(double creditAssignmentPerecent) {
		this.creditAssignmentPerecent = creditAssignmentPerecent;
	}
	
}
